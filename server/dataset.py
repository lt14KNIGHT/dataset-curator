# ---------------------------------------------------------------------------
# Registry
# ---------------------------------------------------------------------------
_TASK_MAP: Dict[str, List[Dict]] = {
    "html_strip": HTML_RECORDS,
    "pii_redact": PII_RECORDS,
    "quality_audit": QA_RECORDS,
}

VALID_TASKS = list(_TASK_MAP.keys())


def get_task_records(task: str) -> List[Dict]:
    """Return a fresh copy of the record list for the given task."""
    if task not in _TASK_MAP:
        raise ValueError(f"Unknown task '{task}'. Valid tasks: {VALID_TASKS}")
    return [dict(r) for r in _TASK_MAP[task]]


def get_all_records() -> List[Dict]:
    """Return all records across all tasks."""
    out = []
    for records in _TASK_MAP.values():
        out.extend(dict(r) for r in records)
    return out
